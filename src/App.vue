<script setup>
import { useMachine } from '@xstate/vue'
import { createMachine } from 'xstate'

const toggleMachine = createMachine({
  id: 'toggle',
  initial: 'inactive',
  states: {
    inactive: {
      on: { TOGGLE: 'active' }
    },
    active: {
      on: { TOGGLE: 'inactive' }
    }
  }
})

const { state, send } = useMachine(toggleMachine)
</script>

<template>
  <button @click="send('TOGGLE')">state is: {{ state.value }}</button>
</template>

